#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	int r;

	i = 1;
	r = 0;
	while (*str == ' ' || *str == '\n' || *str == '\v'
			|| *str == '\f' || *str == '\r' || *str == '\t')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i *= -1;
		str++;
	}
	if (*str == '-' || *str == '+')
		return (0);
	while (*str >= '0' && *str <= '9')
		r = r * 10 + ((*str++) - '0');
	return (i * r);
}
